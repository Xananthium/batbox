// src/commands/TerminalSetupCmd.cpp
//
// batbox::commands::TerminalSetupCmd — implements the /terminal-setup command.
//
// Probes the terminal environment and reports on:
//   • $TERM / $TERM_PROGRAM / $TERM_PROGRAM_VERSION identification
//   • Truecolor support ($COLORTERM == "truecolor" or "24bit")
//   • Italic text support (most modern terminals support VT100 italic)
//   • Mouse mode (reports whether TERM_PROGRAM suggests mouse-capable terminal)
//   • Kitty keyboard protocol detection ($KITTY_WINDOW_ID)
//   • Unicode support ($LANG / locale UTF-8 check)
//
// It then writes per-terminal configuration snippets to
//   ctx.config_dir / "terminal-snippets" / <terminal>.sh
// for the detected terminal (iTerm2, Alacritty, Kitty, WezTerm, others).
//
// Subcommands:
//   /terminal-setup            — probe + print + write snippet
//   /terminal-setup --dry-run  — probe + print only, do not write snippet
//
// No aliases.
//
// Registration entry point:
//   void register_terminal_setup_cmd(SlashCommandRegistry&);

#include <batbox/commands/ISlashCommand.hpp>
#include <batbox/commands/SlashCommandRegistry.hpp>
#include <batbox/core/Result.hpp>
